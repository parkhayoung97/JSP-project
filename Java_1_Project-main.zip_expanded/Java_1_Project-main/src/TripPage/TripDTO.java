package TripPage;


public class TripDTO {
	private int num;
	private String tripuser;
	private String startcode;
	private String endcode;
	private String go;
	private String back;
	private String peple;
	private String seatclax;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getTripuser() {
		return tripuser;
	}

	public void setTripuser(String tripuser) {
		this.tripuser = tripuser;
	}

	public String getStartcode() {
		return startcode;
	}

	public void setStartcode(String startcode) {
		this.startcode = startcode;
	}

	public String getEndcode() {
		return endcode;
	}

	public void setEndcode(String endcode) {
		this.endcode = endcode;
	}

	public String getGo() {
		return go;
	}

	public void setGo(String go) {
		this.go = go;
	}

	public String getBack() {
		return back;
	}

	public void setBack(String back) {
		this.back = back;
	}

	public String getPeple() {
		return peple;
	}

	public void setPeple(String peple) {
		this.peple = peple;
	}

	public String getSeatclax() {
		return seatclax;
	}

	public void setSeatclax(String seatclax) {
		this.seatclax = seatclax;
	}

}